package Assignment1;

public class IncreDecre {

	public static void main(String[] args) {
		int a = 2;
		System.out.println(a++);

		System.out.println(a);

		System.out.println(++a);

		System.out.println(a);

		System.out.println(--a);

		System.out.println(a);

		System.out.println(a--);

		System.out.println(a);

	}

}
